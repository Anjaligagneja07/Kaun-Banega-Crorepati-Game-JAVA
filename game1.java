        public void startTimer()
{
    time = 30;
    jLabel8.setText(String.valueOf(time));
    t = new Timer(1000,new ActionListener()
    {
        public void actionPerformed(ActionEvent e)
        {
            time--;
            jLabel8.setText(String.valueOf(time));
            if(time <= 10)
            {
                jLabel8.setForeground(Color.RED);
            }
            else
            {
                jLabel8.setForeground(Color.WHITE);
            }
            if(time <= 5)
            {
                if(time % 2 == 0)
                {
                    jLabel8.setForeground(Color.RED);
                }
                else
                {
                    jLabel8.setForeground(Color.YELLOW);
                }
            }
            if(time == 0)
            {
                t.stop();
                dispose();
                gameover g = new gameover(Score,email,playername);
                g.setSize(1880,870);
                g.setVisible(true);
            }
        }
    });
    t.start();
}
        private void loadPlayerPhoto()
{
    try
    {
        Class.forName("com.mysql.jdbc.Driver");
        Connection con =DriverManager.getConnection("jdbc:mysql://localhost:3306/kbc_game","root","");
        PreparedStatement ps =con.prepareStatement("SELECT Photo FROM register WHERE Email=?");
        ps.setString(1,email);
        ResultSet rs = ps.executeQuery();
        if(rs.next())
        {
            Blob blob = rs.getBlob("photo");
            if(blob != null)
            {
                byte[] imageBytes =blob.getBytes(1,(int)blob.length());
                ImageIcon icon =new ImageIcon(imageBytes);
                Image img =icon.getImage().getScaledInstance(jLabel2.getWidth(),jLabel2.getHeight(),Image.SCALE_SMOOTH);
                jLabel2.setIcon(new ImageIcon(img));
            }
        }
        rs.close();
        ps.close();
        con.close();
    }
    catch(Exception e)
    {
        System.out.println(e);
    }
}
