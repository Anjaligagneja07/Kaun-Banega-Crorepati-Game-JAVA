         JRadioButton arr[] = {c1, c2, c3, c4};
         int correctIndex = -1;
         for(int i=0; i<4; i++)
         {
             if(arr[i].getText().equals(correctanswer))
             {
                 correctIndex = i;
                 break;
             }
         }
         int randomWrong;
         do
         {
             randomWrong = (int)(Math.random()*4);
         }
         while(randomWrong == correctIndex);
         for(int i=0; i<4; i++)
         {
             if(i != correctIndex && i != randomWrong)
             {
                 arr[i].setVisible(false);
             }
         }
         l7.setIcon(new javax.swing.ImageIcon(getClass().getResource("/game/50.png")));
         jButton2.setEnabled(false);
